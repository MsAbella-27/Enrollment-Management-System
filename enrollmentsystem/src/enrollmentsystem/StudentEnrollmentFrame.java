    private void btnNextAcademicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextAcademicActionPerformed
        // GET INPUTS
        String school = txtSchoolName.getText().trim();
        String strand = cmbStrand.getSelectedItem().toString();
        String average = txtGeneralAverage.getText().trim();
        String graduationYear = txtGraduationYear.getText().trim();
        String honors = txtHonors.getText().trim();
        String achievements = txtAchievements.getText().trim();

        // VALIDATIONS

        if (school.isEmpty()) {
            JOptionPane.showMessageDialog(this, "School Name is required!");
            txtSchoolName.requestFocus();
            return;
        }

        if (average.isEmpty()) {
            JOptionPane.showMessageDialog(this,"General Average is required!");
            txtGeneralAverage.requestFocus();
            return;
        }

        try {

            double avg =
            Double.parseDouble(average);

            // NOT LOWER THAN 75
            if (avg < 75) {

                JOptionPane.showMessageDialog(
                    this,
                    "GWA cannot be lower than 75!"
                );

                return;
            }

            // NOT HIGHER THAN 100
            if (avg > 100) {

                JOptionPane.showMessageDialog(
                    this,
                    "GWA cannot exceed 100!"
                );

                return;
            }

        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(
                this,
                "GWA must be a number!"
            );

            return;
        }

        if (graduationYear.isEmpty()) {
            JOptionPane.showMessageDialog(this,"Graduation Year is required!");
            txtGraduationYear.requestFocus();
            return;
        }

        if (honors.isEmpty()) {
            JOptionPane.showMessageDialog( this,"Honors/Awards is required!");
            txtHonors.requestFocus();
            return;
        }

        if (achievements.isEmpty()) {
            JOptionPane.showMessageDialog(this,"Achievements is required!");
            txtAchievements.requestFocus();
            return;
        }

        // SAVE TO DATASTORE

        DataStore.schoolName = school;
        DataStore.strand = strand;
        DataStore.generalAverage = average;
        DataStore.graduationYear = graduationYear;
        DataStore.honors = honors;
        DataStore.achievements = achievements;

        // NEXT TAB

        tabEnrollment.setSelectedIndex(2);
    }//GEN-LAST:event_btnNextAcademicActionPerformed

