    private void btnEnrollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnrollActionPerformed
            // GET INPUTS
    String firstChoice = cmbProgramFirstChoice.getSelectedItem().toString();
    String secondChoice = cmbProgramSecondChoice.getSelectedItem().toString();
    String thirdChoice = cmbProgramThirdChoice.getSelectedItem().toString();

    // VALIDATIONS

    if (firstChoice.equals(secondChoice) || firstChoice.equals(thirdChoice) || secondChoice.equals(thirdChoice)) {
        JOptionPane.showMessageDialog(this,"Program choices must be different!");
        return;
    }

    // SAVE TO DATASTORE

    DataStore.firstChoice = firstChoice;
    DataStore.secondChoice = secondChoice;
    DataStore.thirdChoice = thirdChoice;
    
    
try {

    Connection conn = DBConnection.getConnection();

    String fullName = txtFullName.getText();

    String programfirstChoice = cmbProgramFirstChoice.getSelectedItem().toString();
    String programsecondChoice = cmbProgramSecondChoice.getSelectedItem().toString();
    String programthirdChoice = cmbProgramThirdChoice.getSelectedItem().toString();

    String status = "Enrolled";

    String sql = "INSERT INTO student(full_name, first_choice, second_choice, third_choice, status) VALUES (?, ?, ?, ?, ?)";

    PreparedStatement pst = conn.prepareStatement(sql);

    pst.setString(1, fullName);
    pst.setString(2, firstChoice);
    pst.setString(3, secondChoice);
    pst.setString(4, thirdChoice);
    pst.setString(5, status);

    pst.executeUpdate();

    JOptionPane.showMessageDialog(null, "Enrollment Successful!");

} catch (Exception e) {

    e.printStackTrace();

}
    // OPEN SUMMARY FRAME

    StudentDashboardFrame dashboard = new StudentDashboardFrame();
    dashboard.setVisible(true);
    dispose();

    }//GEN-LAST:event_btnEnrollActionPerformed

