    private void btnNextStepPersonalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextStepPersonalActionPerformed
         // GET INPUTS
    String fullName = txtFullName.getText().trim();
    String age = txtAge.getText().trim();
    String gender = cmbGender.getSelectedItem().toString();
    String religion = txtReligion.getText().trim();
    String address = txtAddress.getText().trim();
    String contact = txtContactNumber.getText().trim();
    String email = txtEmail.getText().trim();

    // VALIDATIONS

    if (fullName.isEmpty()) {
        JOptionPane.showMessageDialog( this, "Full Name is required!");
        txtFullName.requestFocus();
        return;
    }

    if (age.isEmpty()) {
        JOptionPane.showMessageDialog(this,"Age is required!");
        txtAge.requestFocus();
        return;
    }
    
        // CHECK IF NUMBER
    try {

        int studentAge = Integer.parseInt(age);

        if (studentAge <= 0) {

            JOptionPane.showMessageDialog(
                    this,
                    "Invalid Age!"
            );

            return;
        }

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(
                this,
                "Age must be a number!"
        );

        txtAge.requestFocus();

        return;
    }
    
     // =====================================================
    // GENDER VALIDATION
    // =====================================================

    if (gender.equals("Select Gender")) {

        JOptionPane.showMessageDialog(
                this,
                "Please select gender!"
        );

        return;
    }

    if (religion.isEmpty()) {
        JOptionPane.showMessageDialog(this,"Religion is required!");
        txtReligion.requestFocus();
        return;
    }

    if (address.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Address is required!");
        txtAddress.requestFocus();
        return;
    }

    if (contact.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Contact Number is required!");
        txtContactNumber.requestFocus();
        return;
    }
    
    // MUST BE 11 DIGITS
    if (!contact.matches("\\d{11}")) {

        JOptionPane.showMessageDialog(
                this,
                "Contact Number must be 11 digits!"
        );

        txtContactNumber.requestFocus();

        return;
    }

    if (email.isEmpty()) {
        JOptionPane.showMessageDialog(this,"Email is required!");
        txtEmail.requestFocus();
        return;
    }
    
        if (!email.contains("@")) {

        JOptionPane.showMessageDialog(
                this,
                "Email must contain @"
        );

        txtEmail.requestFocus();

        return;
    }

    // SAVE TO DATASTORE

    DataStore.fullName = fullName;
    DataStore.age = age;
    DataStore.gender = gender;
    DataStore.religion = religion;
    DataStore.address = address;
    DataStore.contactNumber = contact;
    DataStore.email = email;

    // GO TO NEXT TAB

    tabEnrollment.setSelectedIndex(1);
    }//GEN-LAST:event_btnNextStepPersonalActionPerformed

